runDefaults = function(task, learner) {

  oml.run = runTaskMlr(task = task, learner = learner)
    
  if(DEBUG) {
    run.id = 9999
  } else {
    run.id = uploadOMLRun(run = oml.run, upload.bmr = TRUE)
  }
    
  perf = getBMRAggrPerformances(oml.run$bmr, as.df=TRUE)[,-c(1:2)]
  ret = cbind(run.id, perf)
  return(ret)

}

